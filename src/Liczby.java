public class Liczby {
    public static void main(String[] args) {
        int m = 100;
        System.out.println("szesnastkowy:  " +  Integer.toHexString(m));
        System.out.println("ósemkowy:  " +  Integer.toOctalString(m));
        System.out.println("dwójkowy:  " +  Integer.toBinaryString(m));
    }


}

